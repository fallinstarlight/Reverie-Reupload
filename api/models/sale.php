<?php
class sale{
    /* Sale model, represents a sale in the system, used to create sale objects from database records */
    public int $id;
    public string $date;
    public float $amount;
    public int $employeeID;

    /* Constructor to create a sale object from an associative array */
    public function __construct(array $data)
    {
        $fields = ['sale_id', 's_date', 's_amount', 's_saler'];
        foreach($fields as $field){
            if(!isset($data[$field])){
                throw new InvalidArgumentException("Missing info: {$field}");
            }
        }
        
        if(!is_numeric($data['sale_id']) || !is_numeric($data['s_amount']) || !is_numeric($data['s_saler'])){
            throw new InvalidArgumentException("Incorrect ID: {$data['sale_id']}, must be numeric");
        }

        /* If all validations pass, set the sale properties */
        $this -> id = (int)$data['sale_id'];
        $this -> date = $data['s_date'];
        $this -> amount = (float)$data['s_amount'];
        $this -> employeeID = (int)$data['s_saler']; 
    }

} 


