<?php

class dailyreport{
    /* Daily report model, represents a daily report in the system, used to create daily report objects from database records */
    public string $date;
    public float $moneyMade;
    public string $mostSoldProduct;
    public int $totalSales;

    /* Constructor to create a daily report object from an associative array */
    public function __construct(array $data)
    {
        $fields = ['dr_date', 'dr_moneyGained', 'dr_mostSoldProduct', 'dr_totalSales'];
        foreach($fields as $field){
            if(!isset($data[$field])){
                throw new InvalidArgumentException("Missing info: {$field}");
            }
        }
        
        if(!is_string($data['dr_date'])){
            throw new InvalidArgumentException("Incorrect date: {$data['dr_date']}, must be a string");
        }
        if(!is_numeric($data['dr_moneyGained'])){
            throw new InvalidArgumentException("Incorrect money made: {$data['dr_moneyGained']}, must be numeric");
        }
        if(!is_string($data['dr_mostSoldProduct'])){
            throw new InvalidArgumentException("Incorrect most sold product: {$data['dr_mostSoldProduct']}, must be a string");
        }

        /* If all validations pass, set the daily report properties */
        $this -> date = $data['dr_date'];
        $this -> moneyMade = (float)$data['dr_moneyGained'];
        $this -> mostSoldProduct = $data['dr_mostSoldProduct']; 
        $this -> totalSales = $data['dr_totalSales'];
    }
}


