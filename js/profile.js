let currentEmployee = null;

const profileForm = document.getElementById('profileForm');
const salesBody = document.getElementById('profileSalesBody');
const resetProfileBtn = document.getElementById('resetProfileBtn');

function fillProfile(employee) {
    currentEmployee = employee;

    const fullName = `${employee.name || ''} ${employee.surname || ''}`.trim();
    document.getElementById('profileFullName').textContent = fullName || 'Empleado';
    document.getElementById('profileUser').textContent = employee.username ? `@${employee.username}` : '@usuario';
    document.getElementById('profileShift').textContent = employee.shift || '--';
    document.getElementById('profileRole').textContent = employee.role === 'administrator' ? 'admin' : 'empleado';
    document.getElementById('profileToday').textContent = App.money(employee.todaySales);
    document.getElementById('profileTotal').textContent = App.money(employee.totalSales);
    document.getElementById('profilePhoto').src = App.photoPath(employee.photo, 'assets/photos/profile.png');

    profileForm.elements.Name.value = employee.name || '';
    profileForm.elements.Surname.value = employee.surname || '';
    profileForm.elements.Username.value = employee.username || '';
    profileForm.elements.Shift.value = employee.shift || '';
    profileForm.elements.Phone.value = '';
    profileForm.elements.Password.value = '';
    profileForm.elements.PhotoFile.value = '';
}

async function payloadFromProfile() {
    const payload = {};
    const allowed = ['Name', 'Surname', 'Username', 'Shift', 'Phone', 'Password'];

    allowed.forEach(name => {
        const value = App.field(profileForm, name);

        if (value !== '') {
            payload[name] = value;
        }
    });

    const photoFile = profileForm.elements.PhotoFile.files[0];

    if (photoFile) {
        payload.Photo = await App.uploadPhoto('employees', photoFile);
    }

    return payload;
}

function renderSales(sales) {
    if (!sales.length) {
        salesBody.innerHTML = '<tr><td colspan="3">No hay ventas registradas.</td></tr>';
        return;
    }

    salesBody.innerHTML = sales.slice(0, 8).map(sale => `
        <tr>
            <td>${App.escapeHtml(sale.date || '--')}</td>
            <td>#${App.escapeHtml(sale.id || '')}</td>
            <td>${App.money(sale.amount)}</td>
        </tr>
    `).join('');
}

async function loadProfile() {
    try {
        const employee = App.toArray(await App.request('currentemployee'))[0];

        if (employee) {
            fillProfile(employee);
        }
    } catch (error) {
        App.notify(error.message, 'error');
    }
}

async function loadSales() {
    try {
        renderSales(App.toArray(await App.request('salesbycurrentemployee')));
    } catch (error) {
        if (error.status === 404) {
            renderSales([]);
            return;
        }

        salesBody.innerHTML = `<tr><td colspan="3">${App.escapeHtml(error.message)}</td></tr>`;
    }
}

async function saveProfile(event) {
    event.preventDefault();

    try {
        const payload = await payloadFromProfile();

        if (!Object.keys(payload).length) {
            App.notify('No hay cambios para guardar.', 'error');
            return;
        }

        await App.request('currentemployee', {
            method: 'PUT',
            body: payload
        });

        App.notify('Perfil actualizado.', 'success');
        await loadProfile();
    } catch (error) {
        App.notify(error.message, 'error');
    }
}

document.addEventListener('DOMContentLoaded', () => {
    profileForm.addEventListener('submit', saveProfile);
    resetProfileBtn.addEventListener('click', () => currentEmployee && fillProfile(currentEmployee));
    profileForm.elements.PhotoFile.addEventListener('change', () => {
        const file = profileForm.elements.PhotoFile.files[0];

        if (file) {
            document.getElementById('profilePhoto').src = URL.createObjectURL(file);
        }
    });
    loadProfile();
    loadSales();
});


