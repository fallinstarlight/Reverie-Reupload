<?php
include '../config/connection.php';
header('Content-Type: application/json');

/* This script validates user login credentials and sets up the user session */

/* Check if the request method is POST */
if ($_SERVER['REQUEST_METHOD'] !== 'POST') {
    http_response_code(405);
    echo json_encode(['error' => 'Method not allowed']);
    exit();
}
/* Decode the JSON payload that contains the login credentials */
$input = json_decode(file_get_contents('php://input'), true);

/* Validate the presence of required fields */
if (!isset($input['username']) || !isset($input['password'])) {
    http_response_code(400);
    echo json_encode(['error' => 'Username and password are required']);
    session_abort();
    exit();
}

/* Extract and sanitize the login credentials */
$username = trim($input['username']);
$password = $input['password'];
$logindate = date('Y-m-d');

/* Prepare the SQL statement to fetch user information, we use prepared statements to prevent SQL injection */
$stmt = $conn->prepare("SELECT employee_id, e_username, e_passwordHash, e_role, CONCAT(e_name, ' ', e_surname) as e_name, state FROM employees WHERE e_username = ?");
/* Check that the statement was prepared successfully */
if (!$stmt) {
    http_response_code(500);
    echo json_encode(['error' => 'Database query preparation failed: ' . $conn->error]);
    session_abort();
    exit();
}

/* Prepare the SQL statement to update the last login date */
$updateLoginDate = $conn->prepare("UPDATE employees SET last_login = ? WHERE e_username = ?");

/* Bind parameters for the login date update */
$updateLoginDate->bind_param("ss", $logindate, $username);

/* Bind parameter for the user fetch */
$stmt->bind_param("s", $username);
/* Execute the statement */
$stmt->execute();
/* Get the credentials from the database */
$result = $stmt->get_result();
$user = $result->fetch_assoc();

/* 
    Validate the user credentials 
    Uses passwrord_verify to compare the provided password with the stored hash, 
    but also allows for plaintext comparison for legacy passwords and testing purposes. 
    This should be phased out in production for security reasons. 
*/
if ($user && (password_verify($password, $user['e_passwordHash'])) && $user['state'] == 'alive') {
    auth::regenerate(); // Regenerate session ID to prevent session fixation attacks

    /* Set user session variables */
    auth::set('user_id', $user['employee_id']);
    auth::set('username', $user['e_username']);
    auth::set('role', $user['e_role']);
    auth::set('full_name', $user['e_name']);
    auth::set('LAST_ACTIVITY', time());

    /* Return success response */
    echo json_encode([
        'success' => true,
        'message' => 'Login successful',
        'user' => [
            'id' => $user['employee_id'],
            'username' => $user['e_username'],
            'role' => $user['e_role'],
            'full_name' => $user['e_name']
        ]
    ]);

    /* Update the last login date for the user */
    $updateLoginDate->bind_param("ss", $logindate, $username);
    $updateLoginDate->execute();
} else if ($user && $user['state'] == 'discharged') {
    http_response_code(401);
    echo json_encode([
        'success' => false,
        'error' => 'You have been discharged from the system'
    ]);
    session_abort();
} else {
    /* Return error response for invalid credentials */
    http_response_code(401);
    echo json_encode([
        'success' => false,
        'error' => 'Invalid username or password'
    ]);
    session_abort(); // Abort the session to prevent any session data from being set
}

//always close the statement and connection to free up resources
$stmt->close();
$conn->close();


