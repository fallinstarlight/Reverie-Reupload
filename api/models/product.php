<?php
/* Product model, represents a product in the system, used to create product objects from database records */

class product{
    /* Product properties */
    public $code;
    public $name;
    public $price;
    public $description;
    public $amount;
    public $type;
    public $category;
    public $photo;
    public $state;

    /* Constructor to create a product object from an associative array */
    public function __construct(array $data, $code){       
        if(!is_string($code) || strlen($code) > 10){
            throw new InvalidArgumentException("Incorrect code: {$code}, must be string and less than 10 characters");
        }
        if(isset($data['Name']) && (!is_string($data['Name']) || strlen($data['Name']) > 50)){
            throw new InvalidArgumentException("Invalid name: {$data['Name']} must be string and less than 50 characters");
        }
        if(isset($data['Description']) && (!is_string($data['Description']) || strlen($data['Description']) > 255)){
            throw new InvalidArgumentException("Invalid description: {$data['Description']} must be string and less than 255 characters");
        }
        if(isset($data['Price']) && (!is_numeric($data['Price']) || $data['Price'] < 0)){ //validate that price is a number and is greater than 0
            throw new InvalidArgumentException("Invalid price: {$data['Price']} must be a number and must be greater than 0");
        }
        if(isset($data['Category']) && (!is_int($data['Category']) || $data['Category'] <= 0)){
            throw new InvalidArgumentException("The provided category must be a positive integer value");
        }
        if(isset($data['Amount']) && (!is_numeric($data['Amount']) || $data['Amount'] < 0)){
            throw new InvalidArgumentException("Invalid amount: {$data['Amount']} must be an integer and must be greater than or equal to 0");
        }
        if(isset($data['Photo']) && (!is_string($data['Photo']) || strlen($data['Photo']) > 255)){
            throw new InvalidArgumentException("The path to the photo shall not excede 255 characters");
        }

        /* If all validations pass, set the product properties */
        $this -> code = isset($code) ? $code : null;
        $this -> name = isset($data['Name']) ? $data['Name'] : null;
        $this -> price = isset($data['Price']) ? (float)$data['Price'] : null;
        $this -> description = isset($data['Description']) ? $data['Description'] : null;
        $this -> amount = isset($data['Amount']) ? (int)$data['Amount'] : null;
        $this -> type = isset($data['Type']) ? $data['Type'] : null;
        $this -> category = isset($data['Category']) ? (int)$data['Category'] : null;
        $this -> state = (isset($data['State'])) ? $data['State'] : null;
        $this -> photo = (isset($data['Photo'])) ? $data['Photo'] : null;
    }
}


