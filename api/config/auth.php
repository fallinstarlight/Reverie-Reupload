<?php
/* Set of functions for handling user authentication and session management */

class auth
{
    private static $timeout = 1800; // 30 minutes

    /* Start the session and check for timeout */
    public static function start()
    {
        if (session_status() === PHP_SESSION_NONE) {
            ini_set('session.cookie_httponly', 1);
            ini_set('session.use_only_cookies', 1);
            ini_set('session.cookie_secure', isset($_SERVER['HTTPS'])); // Enable if HTTPS
            session_start();
        }

        self::checkTimeout();
    }

    /* Check if the session has timed out, only 30 minutes of inactivity before shutting down the session */
    private static function checkTimeout()
    {
        if (isset($_SESSION['LAST_ACTIVITY'])) {
            $inactiveTime = time() - $_SESSION['LAST_ACTIVITY'];

            if ($inactiveTime > self::$timeout) {
                self::destroy();
                self::start();
                return false;
            }
        }

        $_SESSION['LAST_ACTIVITY'] = time();
        return true;
    }

    /* Regenerate the session ID to prevent session fixation attacks */
    public static function regenerate()
    {
        session_regenerate_id(true);
    }

    /* Destroy the session */
    public static function destroy()
    {
        $_SESSION = array();

        if (ini_get("session.use_cookies")) {
            $params = session_get_cookie_params();
            setcookie(
                session_name(),
                '',
                time() - 42000,
                $params["path"],
                $params["domain"],
                $params["secure"],
                $params["httponly"]
            );
        }

        session_destroy();
    }

    public static function set($key, $value)
    {
        $_SESSION[$key] = $value;
    }

    public static function get($key, $default = null)
    {
        return $_SESSION[$key] ?? $default;
    }

    public static function isLoggedIn()
    {
        return isset($_SESSION['user_id']);
    }

    /* Function to require login, used to prevent access to protected resources */
    /* returnJson parameter to know if send a json response or not, depending on the context that the function is called */
    public static function requireLogin($returnJson = false)
    {
        self::start();
        if (!self::isLoggedIn()) {
            /* JSON response is required in API endpoints */
            if ($returnJson) {
                http_response_code(401);
                echo json_encode(["status" => "error", "error" => "Authentication required"]);
                exit();
            } else {
                /* For regular pages, redirect to login page instead of sending a JSON response */
                header('Location: login.php');
                exit();
            }
        }
    }

    /* Function that checks if the user is an administrator and if so, allow access to admin resources */
    public static function requireAdmin($returnJson = false)
    {
        self::requireLogin($returnJson);
        if (self::get('role') !== 'administrator') {
            if ($returnJson) {
                http_response_code(403);
                echo json_encode(["status" => "error", "error" => "Access denied. Admin privileges required."]);
                exit();
            } else {
                http_response_code(403);
                echo "Access denied. Admin privileges required.";
                exit();
            }
        }
    }

    /* 
        Function to require employee privileges, used to prevent access to protected resources, 
        both admin and employees have access to resources with employee privileges 
    */
    public static function requireEmployee($returnJson = false)
    {
        self::requireLogin($returnJson);
        $role = self::get('role');
        if ($role !== 'employee' && $role !== 'administrator') {
            if ($returnJson) {
                http_response_code(403);
                echo json_encode(["status" => "error", "error" => "Access denied. Employee privileges required."]);
                exit();
            } else {
                http_response_code(403);
                echo "Access denied. Employee privileges required.";
                exit();
            }
        }
    }
}

auth::start();


