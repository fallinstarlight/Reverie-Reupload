/*
 * Function to get daily reports for a specified interval
 * @param $conn Database connection
 * @param $interval Interval in days
*/
function getDailyReport($conn, $interval){
    if(!is_numeric($interval) || $interval < 1){
        http_response_code(400);
        echo json_encode(["status" => "error", "message" => "Invalid interval, must be a positive integer"]);
        exit();
    }
    $query = $conn->prepare("SELECT * FROM daily_report WHERE dr_date >= CURDATE() - INTERVAL ? DAY");
    if (!$query) {
        http_response_code(400);
        echo json_encode(["status" => "error", "message" => "Error with statement"]);
    } else {
        $query->bind_param("i", $interval);
        $query->execute();
        $rows = $query->get_result();
        $reports = getResult($rows);
        getResponse($reports);
    }
    $query->close();
    $conn->close();
}


function getResult($rows): array
{
    $reports = [];
    $error = [];
    if ($rows->num_rows > 0) {
        while ($row = $rows->fetch_assoc()) {
            try {
                $r = new dailyreport($row);
                array_push($reports, $r);
            } catch (InvalidArgumentException $e) {
                array_push($error, $e->getMessage());
                return $error;
            }
        }
    }
    return $reports;
}

function getResponse($report){
    if (empty($report)) {
        http_response_code(404);
        echo json_encode(["status" => "error", "message" => "No matching records found"]);
    } else {
        echo json_encode($report);
    }
}

